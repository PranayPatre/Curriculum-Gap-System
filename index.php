             <h2 class="text-center">Curricullum Gap Analysis</h2>
             <p class="py-5">VCET is affiliated to Mumbai University therefore syllabus is designed by team of BOS (Board of Studies) members along with all senior faculties & industrialists from various reputed institutes & organizations. The curriculum comprises of basic, core & advanced computing subjects related to respective engineering discipline. All course outcomes (COs) of all programs of the institute are mapped with twelve Program Outcomes (POs) defined by National Board of Accreditation’s (NBA) graduate attributes (GA) and two Program Specific Outcomes (PSOs) and if some components, to attain COs/ POs/ PSOs, are not included in the curriculum provided by the affiliated university then the Institution makes additional efforts to impart such knowledge by covering aspects through “Content Beyond Syllabus (CBS)”. Content beyond syllabus are conducted by doing proper “GAP analysis” process to bridge the Gaps between the curriculums. Departments conducts meetings along with department academic & quality assurance committees i.e. DQC, PAC members to align the POs/PSOs with University curriculum for identification of the gaps. Each faculty also develops major elements of POs and PSOs to determine the level of mapping (Strong -3, Moderate-2, Low-1) to POs of the courses. Discussions focused on whether mapping to low-1 of introductory nature was adequate or does the department need to develop more beyond syllabus topics, introduce additional electives, laboratory experiments etc. to improve the level.

 

During meetings, all faculties surveys the departmental academic functioning and discuses about improvements in curriculum to minimize the gaps. The following process is adopted by departments to minimize the gaps,

 

Identification of the courses from each semester to satisfy at least one PO.
Subject teacher sets the internal assessment question paper based on CO.
 Literature survey carried out for mini projects, major projects and for final year subjects to strengthen PO attainment.
Analyzing the COs and POs mapping, weak areas are pointed out and gaps are identified.
Analyzed the feedback from alumni and industries.
Subject teachers review the syllabus of the courses to detect prerequisites and gaps, if any. </p> 
        
